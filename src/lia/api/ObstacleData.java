package lia.api;

public class ObstacleData {
    public float x;
    public float y;
    public float width;
    public float height;

    public ObstacleData(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
